package Datatypes;

public class DataTypesExample {

	public static void main(String[] args) {
		
		String name = "Amisha"; // String
		int age = 23; // Integer
		double salary = 250000.50; // Decimal number
		char grade = 'A'; // Character
		boolean isEmployed = true; // Boolean

		System.out.println("Name: " + name);
		System.out.println("Age: " + age);
		System.out.println("Salary: " + salary);
		System.out.println("Grade: " + grade);
		System.out.println("Employed: " + isEmployed);

		System.out.println("My name is " + name + ".I am " + age + " years old.I am Graduate with grade " + grade
				+ " And my salary is " + salary + ".");
	}


}
