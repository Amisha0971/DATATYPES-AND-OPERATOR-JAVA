package Operatores;

public class RelationalOperators {

	public static void main(String[] args) {
		
		    int a = 10, b = 20;
	        System.out.println("a equals to  b: " + (a == b));
	        System.out.println("a not equals to b: " + (a != b));
	        System.out.println("a greater than  b: " + (a > b));
	        System.out.println("a smaller than  b: " + (a < b));
	        System.out.println("a greater than equals to  b: " + (a >= b));
	        System.out.println("a smaller than equals to  b: " + (a <= b));

	}

}
